    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String txt = jTextArea1.getText();
        try {
            JAXBContext context = JAXBContext.newInstance(ObjectFactory.class);

            Unmarshaller u = context.createUnmarshaller();
            StringReader reader = new StringReader(txt);
            PetShop petshop = (PetShop) u.unmarshal(reader);
            String  txt2 = "";
            for (Animal animal : petshop.getAnimal()) {
                txt2 +="\n#################";
                txt2 += "\nNome: " + animal.getNome();
                txt2 += "\nEspecie: " + animal.getEspecie();
                txt2 += "\nRaca: "+animal.getRaca();
                txt2 += "\nGenero: " + animal.getGenero();
                //txt2 += "\nData de Nascimento" + animal.getDtNasc();
            }
            jTextArea1.setText(txt2);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }                                        

